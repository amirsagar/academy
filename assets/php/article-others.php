        <h4 class="modal-title">Batman: Dark Victory</h4>

        <div class="modal-body-others">
          <article>
            <img class="pull-right img-rounded" src="assets/images/others/comics/batman-large-8.jpg" alt="">
            <div class="media-body">
              <p><b>Batman: Dark Victory</b> was a 14-part comic book limited series written by Jeph Loeb and by Tim Sale. The series is a sequel to Batman: The Long Halloween and was published originally from 1999-2000 by DC Comics. The plot centers on a series of murders involving Gotham City police officers by a mysterious serial killer only known as The Hangman. Central to the storyline is a territory war between Two-Face and the remnants of the Falcone mob led by Sofia Falcone.
                The story is also a re-telling of the origins of Batman's sidekick Robin and his adoption by Bruce Wayne. The story brings closure to many characters in Frank Miller's Batman: Year One.

                Dark Victory is also a personal favorite of current Batman star Christian Bale.

                In 2004, Loeb and Sale produced a follow-up titled Catwoman: When in Rome, focusing on Catwoman's movements in Italy. The story takes place during the events of Dark Victory, between Valentine's Day (when Catwoman leaves Gotham) and Thanksgiving.
                <hr>
                <h4>Plot </h4>
                During the events of Batman: The Long Halloween (to which Dark Victory is a follow-up), Batman captured and imprisoned Alberto Falcone, the serial killer known as Holiday. Months later, a mass breakout at Arkham Asylum occurred, orchestrated by Pino and Umberto Maroni, the sons of Sal "The Boss" Maroni. The breakout is staged so in the confusion, the two can find Harvey "Two-Face" Dent and kill him as a peace-offering to Sofia Gigante Falcone. In the aftermath, Alberto is granted conditional parole based on his actions during the riot. Soon after, police officers began to die, one on each major holiday, with crude hangman games pinned to their hung corpses and evidence suggesting that Harvey Dent is the killer.
                The new district attorney, Janice Porter, began working on the case while Alberto was put up by his enigmatic brother Mario, who had just returned from exile in Italy. Alberto also took in his sister, Sofia, who barely survived an encounter with Catwoman in The Long Halloween and was confined to a wheelchair due to her injuries. Sofia nevertheless continued to manage the flagging Falcone empire.

                As the Hangman's spree of terror escalated, so too did Two-Face's war with Sofia and her family. The collateral damage of the war included the parents of Dick Grayson, who was taken in by Bruce Wayne. Two-Face enlists the aid of his fellow 'freaks' to destroy the remainders of Gotham's mafia; his lieutenants included The Joker, Scarecrow, The Penguin, The Riddler, Poison Ivy, Mr. Freeze, The Mad Hatter and Solomon Grundy. Mario, who had an alliance with Janice Porter, was soon faced with isolation as Porter began a secret affair with Two-Face and was eventually slain by him. At the same time, however, Two-Face saved Gordon's life when the Hangman tried to hang him on the Batsignal, stating that he wasn't the killer.

